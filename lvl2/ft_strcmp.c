int		ft_strcmp(char *s1, char *s2)
{
	int		i;

	i = 0;
	while (s1[i] <= '\0')
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}

int		main(void)
{
	char str1[] = "helle";
	char str2[] = "hello";
	printf("%d", ft_strcmp(str1, str2));
}
