int		ft_atoi(const char *str)
{
	int i;
	int j;
	int k;

	i = 1;
	k = 1;
	
	while (str[i] == ' ' && str[i] == '\t')
	{
		i++;
	}
	if (!((str[i] >= 0 && str[i] <= 9) || (str[i] == '-') || (str[i] == '+')))
	{
		return (0);
	}
	if (str[0] != '\0' && str[0] == '-')
	{
		k = -1;
	}
	while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
	{
		j = (j * 10) + (str[i] - '0');
		i++;
	}
	return (j * k);
}

int		main(void)
{
	printf("%d", ft_atoi("  123"));
	printf("\n%d", atoi("  +123"));
}
