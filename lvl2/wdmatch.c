#include <unistd.h>

int		main(int argc, char **argv)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (argc == 3 && argv[1][i] != '\0'&& argv[2][j])
	{
		while (argv[1][i] != '\0' && argv[2][j] != '\0')
		{
			if(argv[1][i] == argv[2][j])
			{
				write(1, &argv[1][i], 1);
				break;
			}
			j++;
		}
		j = 0;
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
