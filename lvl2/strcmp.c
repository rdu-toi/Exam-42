int		ft_strcmp(char *s1, char *s2)
{
	while (*s1 && (*s1 == *s2))
	{
		s1++;
		s2++;
	}
	return ((unsigned char)*s1 - (unsigned char)*s2);
}

int 	main(void)
{
	printf("%d", ft_strcmp("\0", "\200"));
	printf("\n%d", strcmp("\0", "\200"));
}
