int		is_power_of_2(unsigned int n)
{
	int i;
	int j;

	i = 1;
	while (i)
	{
		j = i * i;
		if (j == n)
		{
			return (1);
		}
		else if (j > n)
		{
			return (0);
		}
		i++;
	}
}

int		main(void)
{
	printf("%d", is_power_of_2(25));
}
