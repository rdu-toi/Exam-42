#include <unistd.h>

int		main(int argc, char **argv)
{
	int i;
	int j;
	int k;

	i = 0;
	while (argc == 2)
	{
		while (argv[1][i] != '\0')
		{
			j = argv[1][i] + 13;
			k = argv[1][i] - 13;
			if (j >= 'a' || j <= 'm' || j >= 'A' || j <= 'M')
			{
				write(1, &j, 1);
			}
			else if (j >= 'n' || j <= 'z' || j >= 'N' || j <= 'Z')
			{
				write(1, &k, 1);
			}
			else
			{
				write(1, &argv[1][i], 1);
			}
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
