#include <unistd.h>

int		main(int argc, char **argv)
{
	int i;
	int j;
	int up;
	int dn;

	i = 0;
	if (argc == 2)
	{
		while (argv[1][i] != '\0')
		{
			j = argv[1][i];
			if ((j >= 'a' && j <= 'm') || (j >= 'A' && j <= 'M'))
			{
				up = argv[1][i] + 13;
				write(1, &up, 1);
			}
			else if ((j >= 'n' && j <= 'z') || (j >= 'N' && j <= 'Z'))
			{
				dn = argv[1][i] - 13;
				write(1, &dn, 1);
			}
			else
			{
				write(1, &argv[1][i], 1);
			}
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
